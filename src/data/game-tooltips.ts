export function getResourceInfo(id: string): ResourceInfo | undefined {
  return resourcesData.find(r => r.id === id)
}

export function getBuildingInfo(id: string): BuildingInfo | undefined {
  return buildingsData.find(b => b.id === id)
}

export function getWorkerClassInfo(id: string): WorkerClassInfo | undefined {
  return workerClassesData.find(w => w.id === id)
}
